package com.section3.classmanagement;

import java.sql.*;
public class DataBaseController {

    Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/haile", "root", "root");
    Statement statement = connection.createStatement();
    ResultSet resultSet = null;
    public DataBaseController() throws SQLException {
    }

    public void checkCredential() throws SQLException {
        resultSet = statement.executeQuery("select * from teacher;");

        while(resultSet.next()) {
            String name = resultSet.getString("username");
            int password = resultSet.getInt("password");

            System.out.println("Name: " + name);
            System.out.println("Password: " + password);
        }
    }
}
